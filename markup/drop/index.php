						<h3><a href="#">The Top 100 Best NGOs - Editorial Word</a></h3>
						<img src="/markup/images/content/drop-image.png" class="fullstory-image" />
						<p>Just what is a non-government organization? While an obvious first question to ask when compiling a list of the type that fills the following pages, the search for an answer is less straightforward than one might assume. To begin with, the very term is defined by a negative – NGOs are not government. At the same time, NGOs are ubiquitous. One need only have witnessed the chaotic free-for-all that ensued after the devastating 2010 earthquake in Haiti to understand how nimble, diffuse and – some might argue – unaccountable this group of actors can be. We all think we know what NGOs are. Yet the cliché of faith or service-based organizations from rich countries selflessly dedicating themselves to the relief and betterment of the world’s impoverished and downtrodden is just that – a tired stereotype – if it were ever true at all.</p>
						<p>In the process of researching, refining and ranking our Top 100 list, the team at The Global Journal learnt much about just how diverse, dynamic and innovative the NGO sector truly is. From training rats to sniff out landmines in Mozambique, to using sub-titled Bollywood music videos as a cheap and effective mass literacy tool, our world is awash with ground-breaking ideas and - luckily - more often than not the donors or lenders willing to take a risk on an untried, but promising, pitch. ‘Social entrepreneurship’, ‘participatory programming’, ‘market based approaches’, ‘rigorous evaluation’, ‘evidence-based design’, ‘self sustaining’: these are just some of the buzz-phrases that emerged time and time again as the team waded through a sea of annual reports, project documentation and testimonials. We are clearly living through a process of pervasive change in the NGO world. Old approaches are being shaken up by the combined influence of technology, the private sector, and what is perhaps best described as a ‘startup culture’.</p>
